package Assignment1;

import java.util.Arrays;

public class ReplaceArrayElement {

	public static void main(String[] args)
	{
		int[] arr = {65,32,675,2,11};
		System.out.println("ArrayList : " +Arrays.toString(arr));
	
//		Replace 1st element at end
		swap(arr, 0, 4);
		System.out.println("Replace 1st<->end : " +Arrays.toString(arr));
		

	}
	static void swap(int[] arr, int index0, int end)
	{
		int a = arr[index0];
		arr[index0] = arr[end];
		arr[end] =a;
	}

}
